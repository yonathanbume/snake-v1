    private void formMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseMoved
        GameParam.positionX = evt.getX();
        
        if (evt.getY() > GameParam.HEADERHIGHT) {
            GameParam.positionY = evt.getY();
        } else {
            GameParam.positionY = GameParam.POSITIONYINIT;
        }
        
        lblPosition.setText("PosX: " + GameParam.positionX + " | PosY: " + GameParam.positionY);
    }//GEN-LAST:event_formMouseMoved

